// VMath.h

#include<iostream>

using namespace std;

class VMath
{
	public:
		// Addition
		void add(int a, int b) {
			return a + b;
		}

		// Subtraction
		void sub(int a, int b) {
			return a - b;
		}

		// Multiplication
		void mul(int a, int b) {
			return a * b;
		}

		// Division
		void div(int a, int b) {
			return a / b;
		}
};
